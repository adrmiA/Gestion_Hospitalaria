    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (txtNombre.getText().trim().isEmpty() || txtUsuario.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nombre y usuario son obligatorios.");
            return;
        }
        btnGuardar.setEnabled(false);
        new SwingWorker<Void, Void>() {
            String err;
            @Override protected Void doInBackground() {
                try {
                    JsonObject u = new JsonObject();
                    u.addProperty("nombre",  txtNombre.getText().trim());
                    u.addProperty("usuario", txtUsuario.getText().trim());
                    String rolSeleccionado = cmbRol.getSelectedItem().toString();
                    Integer idRol = mapaRoles.get(rolSeleccionado);

                    System.out.println("Rol seleccionado: " + rolSeleccionado);
                    System.out.println("IdRol enviado: " + idRol);

                    if (idRol == null) {
                        throw new RuntimeException("El rol seleccionado no es válido.");
                    }

                    u.addProperty("idRol", idRol);
                    String pwd = new String(txtPassword.getPassword()).trim();
                    if (!pwd.isEmpty()) u.addProperty("contraseña", pwd);
                    
                    if (idUsuario == null) {
                        ApiClient.post("/usuarios/register", u);
                    } else {
                        u.addProperty("idUsuario", idUsuario);
                        ApiClient.put("/usuarios/" + idUsuario, u);
                    }
                } catch (Exception ex) { err = ex.getMessage(); }
                return null;
            }
            @Override protected void done() {
                btnGuardar.setEnabled(true);
                if (err != null) JOptionPane.showMessageDialog(UsuarioFormDialog.this, "Error: " + err);
                else { guardado = true; dispose(); }
            }
        }.execute();
    }//GEN-LAST:event_btnGuardarActionPerformed

