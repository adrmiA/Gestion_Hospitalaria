    private java.util.Map<String, Integer> mapaRoles = new java.util.HashMap<>();

    private void cargarRoles() {

        new javax.swing.SwingWorker<com.google.gson.JsonArray, Void>() {

            @Override
            protected com.google.gson.JsonArray doInBackground() {
                try {
                    com.google.gson.JsonElement r = ApiClient.get("/roles");
                    return r.isJsonArray() ? r.getAsJsonArray() : new com.google.gson.JsonArray();
                } catch (Exception e) {
                    e.printStackTrace();
                    return new com.google.gson.JsonArray();
                }
            }

            @Override
            protected void done() {
                try {
                    cmbRol.removeAllItems();
                    mapaRoles.clear();

                    for (com.google.gson.JsonElement el : get()) {
                        com.google.gson.JsonObject obj = el.getAsJsonObject();

                        int id = obj.get("id").getAsInt(); 
                        String nombre = obj.get("nombre").getAsString();

                        cmbRol.addItem(nombre);
                        mapaRoles.put(nombre, id);
                    }

                } catch (Exception e) {
                    e.printStackTrace();
                }
            }

        }.execute();
    }

    private void cargarDatos() {
        new SwingWorker<JsonObject, Void>() {
            @Override protected JsonObject doInBackground() throws Exception {
                JsonElement r = ApiClient.get("/usuarios/" + idUsuario);
                return r.isJsonObject() ? r.getAsJsonObject() : null;
            }
            @Override protected void done() {
                try {
                    JsonObject u = get();
                    if (u == null) return;
                    txtNombre.setText(gs(u, "nombre"));
                    txtUsuario.setText(gs(u, "usuario"));
                    txtPassword.setText("");  // no precargar contraseña
                    cmbRol.setSelectedItem(gs(u, "rol"));
                } catch (Exception ex) { ex.printStackTrace(); }
            }
        }.execute();
    }
    
