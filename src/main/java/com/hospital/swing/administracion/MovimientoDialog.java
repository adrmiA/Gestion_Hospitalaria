    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        Object itemObj = cmbProductos.getSelectedItem();
        if (!(itemObj instanceof InventarioItem)) {
            JOptionPane.showMessageDialog(this, "Seleccione un producto de la lista.");
            return;
        }

        InventarioItem selected = (InventarioItem) itemObj;
        
        if (selected.id <= 0) {
            JOptionPane.showMessageDialog(this, "Error: El ID del producto es 0. Verifique la base de datos.");
            return;
        }

        String cantStr = txtCantidad.getText().trim();
        int cantidad;
        try {
            cantidad = Integer.parseInt(cantStr);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Cantidad inválida.");
            return;
        }

        String tipo = cmbTipo.getSelectedItem().toString();
        String motivo = txtMotivo.getText().trim();

        btnRegistrar.setEnabled(false);

        new SwingWorker<Void, Void>() {
            String err;
            @Override protected Void doInBackground() {
                try {
                    JsonObject mov = new JsonObject();
                    mov.addProperty("IdProducto", selected.id); 
                    mov.addProperty("Cantidad", cantidad);
                    mov.addProperty("TipoMovimiento", tipo);
                    mov.addProperty("Motivo", motivo);

                    ApiClient.post("/inventario/movimiento", mov);
                } catch (Exception ex) { err = ex.getMessage(); }
                return null;
            }
            @Override protected void done() {
                btnRegistrar.setEnabled(true);
                if (err != null) JOptionPane.showMessageDialog(MovimientoDialog.this, "Error: " + err);
                else { 
                    registrado = true; 
                    JOptionPane.showMessageDialog(MovimientoDialog.this, "Movimiento registrado con éxito.");
                    dispose(); 
                }
            }
        }.execute();
    }//GEN-LAST:event_btnRegistrarActionPerformed

