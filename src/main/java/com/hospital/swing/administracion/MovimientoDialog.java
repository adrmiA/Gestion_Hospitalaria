    private void cargarProductos() {
        new SwingWorker<JsonArray, Void>() {
            @Override
            protected JsonArray doInBackground() throws Exception {
                JsonElement r = ApiClient.get("/inventario/status");
                return r.isJsonArray() ? r.getAsJsonArray() : new JsonArray();
            }

            @Override
            protected void done() {
                try {
                    JsonArray data = get();
                    DefaultComboBoxModel<Object> model = new DefaultComboBoxModel<>();
                    
                    for (JsonElement el : data) {
                        JsonObject obj = el.getAsJsonObject();

                        // Extracción robusta de ID
                        int idEncontrado = 0;
                        if (obj.has("idProducto")) idEncontrado = obj.get("idProducto").getAsInt();
                        else if (obj.has("IdProducto")) idEncontrado = obj.get("IdProducto").getAsInt();
                        else if (obj.has("idInventario")) idEncontrado = obj.get("idInventario").getAsInt();
                        else if (obj.has("IdInventario")) idEncontrado = obj.get("IdInventario").getAsInt();

                        // Extracción robusta de Nombre
                        String nombre = "ID: " + idEncontrado;
                        if (obj.has("nombreProducto")) nombre = obj.get("nombreProducto").getAsString();
                        else if (obj.has("NombreProducto")) nombre = obj.get("NombreProducto").getAsString();

                        InventarioItem itemParaCombo = new InventarioItem(idEncontrado, nombre);
                        model.addElement(itemParaCombo);
                        
                        if (idInventarioPreseleccionado != null && idEncontrado == idInventarioPreseleccionado) {
                            model.setSelectedItem(itemParaCombo);
                        }
                    }
                    cmbProductos.setModel((DefaultComboBoxModel) model);
                } catch (Exception ex) {
                    System.err.println("Error en combo: " + ex.getMessage());
                }
            }
        }.execute();
    }
    private static class InventarioItem {
        int id;
        String nombre;
        InventarioItem(int id, String nombre) { this.id = id; this.nombre = nombre; }
        @Override public String toString() { return nombre; }
    }

  
