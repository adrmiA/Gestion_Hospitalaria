    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        if (idExpediente == -1) {
            JOptionPane.showMessageDialog(this, "Primero debe buscar y seleccionar un paciente.");
            return;
        }

        // Crear el objeto para el API
        JsonObject sv = new JsonObject();
        sv.addProperty("IdExpediente", idExpediente);
        sv.addProperty("FrecuenciaCardiaca", txtFC.getText().trim());
        sv.addProperty("FrecuenciaRespiratoria", txtFR.getText().trim());
        sv.addProperty("Temperatura", txtTemp.getText().trim());
        sv.addProperty("PresionArterial", txtPA.getText().trim());
        sv.addProperty("SaturacionOxigeno", txtSat.getText().trim());
        sv.addProperty("Peso", txtPeso.getText().trim());
        sv.addProperty("Talla", txtTalla.getText().trim());

        new SwingWorker<Void, Void>() {
            String errorMsg = null;
            @Override protected Void doInBackground() throws Exception {
                try {
                    ApiClient.post("/signosvitales", sv);
                } catch (Exception e) {
                    errorMsg = e.getMessage();
                }
                return null;
            }
            @Override protected void done() {
                if (errorMsg == null) {
                    JOptionPane.showMessageDialog(SignosVitalesPanel.this, "Signos vitales registrados correctamente.");
                    limpiarCampos();
                    btnBuscarActionPerformed(null); // Recargar la tabla automáticamente
                } else {
                    JOptionPane.showMessageDialog(SignosVitalesPanel.this, "Error al registrar: " + errorMsg);
                }
            }
        }.execute();
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void limpiarCampos() {
        txtFC.setText("");
        txtFR.setText("");
        txtTemp.setText("");
        txtPA.setText("");
        txtSat.setText("");
        txtPeso.setText("");
        txtTalla.setText("");
    }

    private String gs(JsonObject o, String k) {
        return (o.has(k) && !o.get(k).isJsonNull()) ? o.get(k).getAsString() : "";
    }

