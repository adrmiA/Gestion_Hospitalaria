    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
         String dui = txtDUI.getText().trim();
        if (dui.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese un DUI para buscar.");
            return;
        }

        new SwingWorker<Void, Void>() {
            String nombrePac = "";
            JsonArray historial = new JsonArray();
            String errorMsg = null;

            @Override protected Void doInBackground() throws Exception {
                try {
                    // 1. Buscar expediente por DUI
                    JsonElement resE = ApiClient.get("/expedientes/buscar-dui/" + dui);
                    if (resE.isJsonObject()) {
                        JsonObject exp = resE.getAsJsonObject();
                        idExpediente = exp.get("id").getAsInt();
                        nombrePac = exp.get("nombrePaciente").getAsString();

                        // 2. Cargar historial de signos vitales
                        JsonElement resS = ApiClient.get("/signosvitales/expediente/" + idExpediente);
                        if (resS.isJsonArray()) {
                            historial = resS.getAsJsonArray();
                        }
                    } else {
                        idExpediente = -1;
                    }
                } catch (Exception e) {
                    errorMsg = e.getMessage();
                }
                return null;
            }

            @Override protected void done() {
                if (idExpediente != -1) {
                    lblPaciente.setText("Paciente: " + nombrePac);
                    lblPaciente.setForeground(new Color(0, 102, 0));
                    modelo.setRowCount(0);
                    for (JsonElement el : historial) {
                        JsonObject s = el.getAsJsonObject();
                        modelo.addRow(new Object[]{
                            gs(s, "fechaRegistro"), gs(s, "frecuenciaCardiaca"), 
                            gs(s, "frecuenciaRespiratoria"), gs(s, "temperatura"), 
                            gs(s, "presionArterial"), gs(s, "saturacionOxigeno"), 
                            gs(s, "peso"), gs(s, "talla")
                        });
                    }
                } else {
                    lblPaciente.setText("No se encontró el expediente.");
                    lblPaciente.setForeground(Color.RED);
                    modelo.setRowCount(0);
                    if (errorMsg != null) System.err.println("Error: " + errorMsg);
                }
            }
        }.execute();
    }//GEN-LAST:event_btnBuscarActionPerformed

